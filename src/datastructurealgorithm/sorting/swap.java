package datastructurealgorithm.sorting;

public class swap {

    public static void main(String[] args) {
        int number1=40;
        int number2=60;

        int temp;

        System.out.println("Number1 "+number1);
        System.out.println("Number2 "+number2);

        System.out.println("******** After swap **********");
        temp=number1;
        number2=temp;
        number1=number2;
        temp=number1;

        System.out.println("Number1 "+number1);
        System.out.println("Number2 "+number2);
        System.out.println("Temp "+temp);









    }
}

