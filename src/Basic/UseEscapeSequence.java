package Basic;

public class UseEscapeSequence {

	// Escape Sequence:  
	
	public static void main(String[] args) {
		System.out.println("Hello \tWorld");
		System.out.println("\"Hello World\"");
		System.out.println("Hello World1\b");
		System.out.println("Hello World\n");
		System.out.println("welcome\r back");
		System.out.println("welcome back \f");
		System.out.println("We are Learning Escape Sequence");
	}
	
	
	
}
