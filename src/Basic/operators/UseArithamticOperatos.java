package Basic.operators;

public class UseArithamticOperatos {

	// + Addition operator
	// - substruction operator
	// * Multiplication
	// / Division
	// % Remainder
	
	public static void main(String[] args) {
		
		int number1 = 50;
		int number2 = 60;
		
		int summation = number1 + number2;
		System.out.println("Summation value is "+ summation);
		
		int substraction  = number1 - number2;
		System.out.println("Substruction value is "+ substraction);
		
		double multiply  = number1 * number2;
		System.out.println("Multiplication value is "+ multiply);
		
		double division  = number1 / number2;
		System.out.println("Division value is "+ division);
		
		double remainder  = number1 % number2;
		System.out.println("Remainder value is "+ remainder);
		
		// 7/2=  division value 3 remainder 1
	}
	
	
	
	
	
}
